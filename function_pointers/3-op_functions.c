#include "3-calc.h"
/**
 * op_add - function add two integers
 * @a: it is an integer called 'a'
 * @b: it is an integer called 'b'
 * Return: Always integer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function add two integers
 * @a: it is an integer called 'a'
 * @b: it is an integer called 'b'
 * Return: Always integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function add two integers
 * @a: it is an integer called 'a'
 * @b: it is an integer called 'b'
 * Return: Always integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function add two integers
 * @a: it is an integer called 'a'
 * @b: it is an integer called 'b'
 * Return: Always integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function add two integers
 * @a: it is an integer called 'a'
 * @b: it is an integer called 'b'
 * Return: Always integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
